package com.dpdocter.beans;

import java.util.List;

public class NdhmPatient {

	private String id;
	
	private String name;
	
	private String gender;
	
	private String yearOfBirth;
	
	private NdhmAddress address;
	
	private List<AuthConfirmIdentifier> identifier;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getGender() {
		return gender;
	}

	public void setGender(String gender) {
		this.gender = gender;
	}

	public String getYearOfBirth() {
		return yearOfBirth;
	}

	public void setYearOfBirth(String yearOfBirth) {
		this.yearOfBirth = yearOfBirth;
	}

	public NdhmAddress getAddress() {
		return address;
	}

	public void setAddress(NdhmAddress address) {
		this.address = address;
	}

	public List<AuthConfirmIdentifier> getIdentifier() {
		return identifier;
	}

	public void setIdentifier(List<AuthConfirmIdentifier> identifier) {
		this.identifier = identifier;
	}
	
	
	
	
}
