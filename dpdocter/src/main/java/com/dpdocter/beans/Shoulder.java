package com.dpdocter.beans;

public class Shoulder {

	private Integer flexion;
	private Integer extension;
	private Integer abduction;
	private Integer integerRotation;
	private Integer externalRotation;
	public Integer getFlexion() {
		return flexion;
	}
	public void setFlexion(Integer flexion) {
		this.flexion = flexion;
	}
	public Integer getExtension() {
		return extension;
	}
	public void setExtension(Integer extension) {
		this.extension = extension;
	}
	public Integer getAbduction() {
		return abduction;
	}
	public void setAbduction(Integer abduction) {
		this.abduction = abduction;
	}
	public Integer getIntegerRotation() {
		return integerRotation;
	}
	public void setIntegerRotation(Integer integerRotation) {
		this.integerRotation = integerRotation;
	}
	public Integer getExternalRotation() {
		return externalRotation;
	}
	public void setExternalRotation(Integer externalRotation) {
		this.externalRotation = externalRotation;
	}
	
	
	
}
